import turtle
turtle.shape('turtle')
s=turtle.clone()
s.shape('square')
s.penup()
s.goto(100,100)
s.stamp()
s.pendown()
s.goto(200,100)
s.stamp()
s.goto(200,0)
s.stamp()
s.goto(100,0)
s.stamp()
s.goto(100,100)
t=turtle.clone()
t.shape('triangle')
t.penup()
t.goto(-100,0)
t.stamp()
t.pendown()
t.goto(-200,100)
t.stamp()
t.goto(-100,100)
t.stamp()
t.goto(-100,0)

turtle.mainloop()
